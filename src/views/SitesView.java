    private void loadTable(){
        ArrayList<models.Sites> listSites = searchEngine.getListSites();
        Object []obj = new Object[2];
        for(int i = 0; i < listSites.size(); i++){
            obj[0] = listSites.get(i).getTitle();
            obj[1] = listSites.get(i).getAddress();
            model.addRow(obj);
        }
    }

